-- ===== MIGRATION 033: откат owner-условия в materials_insert =====
-- ⚠️ ПРИМЕНИТЬ СРАЗУ — на проде уже есть платящие клиенты.
--
-- Миграция 032 переписала materials_insert как
--     with check (bucket_id = 'materials' and owner = auth.uid())
-- в качестве defense-in-depth (чтобы нельзя было писать объекты «от чужого имени»).
--
-- Проблема: поле storage.objects.owner в Supabase ОФИЦИАЛЬНО DEPRECATED и подлежит
-- удалению — актуальное поле owner_id (и оно типизировано как TEXT, не uuid, что ещё
-- и требует явного каста). Если owner не проставляется при загрузке, WITH CHECK не
-- проходит и ЗАГРУЗКА МАТЕРИАЛОВ ЛОМАЕТСЯ ЦЕЛИКОМ. Материалы — основа генерации
-- контента, то есть это положило бы продукт для живых клиентов.
--
-- Проверить вживую нельзя (в бакете пока нет ни одного файла, у ассистента нет сессии),
-- поэтому выбираем надёжность: возвращаем условие к состоянию, которое заведомо
-- работало до 032. Риск от отката минимален — «чужой» объект без строки в
-- project_materials (а она под RLS) product-wise бесполезен и невидим.
--
-- ГЛАВНЫЙ ФИКС H4 СОХРАНЯЕТСЯ: клиентская DELETE-политика остаётся удалённой — именно
-- она была дырой (project viewer мог прочитать file_url и снести чужой файл через
-- Storage API). Легитимное удаление идёт admin-клиентом в /api/materials, RLS не
-- касается. Так что защита от порчи чужих файлов на месте.
--
-- Идемпотентна: повторный прогон безопасен.

drop policy if exists "materials_insert" on storage.objects;
create policy "materials_insert" on storage.objects
  for insert to authenticated
  with check (bucket_id = 'materials');
