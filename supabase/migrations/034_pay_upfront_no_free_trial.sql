-- ===== MIGRATION 034: модель «плати сразу» — новый юзер без бесплатного триала =====
-- Решение владельца (16 июля, после жалоб): публичная модель = оплата сразу.
--
-- Что было не так: лендинг уже «тариф-онли» (2-месячный доступ — приватная договорённость
-- для отдельных участников, НЕ публичный оффер), а приложение раздавало «Полный доступ на
-- 2 месяца» КАЖДОМУ, кто зарегистрировался (миграция 032 ставила trial_ends_at = now()+60d).
-- Клиенты заходили и пользовались, не заплатив, и не понимали, где вообще выбрать тариф.
--
-- Что делает эта миграция:
--  1) handle_new_user больше НЕ выдаёт 60-дневный триал: trial_ends_at = now(), то есть
--     триал считается истёкшим сразу → isEntitled() = false → юзер упирается в выбор
--     тарифа (баннер + блок генерации), как только включён BILLING_ENFORCED.
--  2) Текущим НЕОПЛАТИВШИМ (role='client' И tier='trial') триал закрывается сейчас.
--
-- Кого НЕ трогает (проверено запросом перед применением):
--   * платящих — tier != 'trial' (solo/pro/producer через Stripe/Продамус);
--   * админов — role='admin' (isEntitled их пропускает всегда);
--   * тестеров Августы — им выдан role='producer' + tier='producer' до 2027, они не на trial.
--
-- ⚠️ ЗАВИСИМОСТЬ: работает только вместе с env BILLING_ENFORCED='true' в Vercel. Без него
--    гейт спит и доступ останется открытым (миграция лишь покажет баннер «подключи тариф»).
-- ⚠️ Триал для избранных выдаётся вручную: profiles.trial_ends_at = нужная дата.
-- Идемпотентна: повторный прогон безопасен.

-- ────────────────────────────────────────────────────────────────────────────
-- 1. Новый юзер регистрируется БЕЗ бесплатного периода
-- ────────────────────────────────────────────────────────────────────────────
create or replace function handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, email, full_name, role, subscription_status, trial_ends_at)
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'full_name', ''),
    case when new.email = current_setting('app.admin_email', true) then 'admin' else 'client' end,
    'trialing',
    now()   -- триала нет: доступ открывается только после оплаты
  );
  return new;
end;
$$ language plpgsql security definer;

-- ────────────────────────────────────────────────────────────────────────────
-- 2. Закрыть триал текущим неоплатившим клиентам
-- ────────────────────────────────────────────────────────────────────────────
update profiles
   set trial_ends_at = now()
 where subscription_tier = 'trial'
   and role = 'client'
   and subscription_status = 'trialing'
   and trial_ends_at > now();   -- только тем, у кого он ещё «идёт»
