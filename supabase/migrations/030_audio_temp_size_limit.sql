-- Аудио-интервью бывают длинные: 50+ минут ≈ 70–80 МБ. Тестер словил «The object
-- exceeded the maximum allowed size» на файлах ~77 МБ.
--
-- Бакет audio-temp задумывался на 200 МБ (миграция 011), НО там
-- `insert ... on conflict (id) do nothing` — если бакет уже существовал с
-- дефолтным лимитом, 200 МБ так и не применились. Явно поднимаем лимит бакета.
update storage.buckets
set file_size_limit = 209715200 -- 200 MB
where id = 'audio-temp';

-- ⚠️ ВАЖНО (действие ВЛАДЕЛЬЦА в дашборде, НЕ покрывается миграцией):
-- лимит бакета КЭПИТСЯ глобальным лимитом загрузки проекта Supabase. По умолчанию
-- он 50 МБ — именно он режет файлы 77 МБ, даже когда бакет разрешает 200 МБ.
-- Поднять: Supabase Dashboard → Project Settings → Storage →
--          «Upload file size limit» → поставить ≥ 200 MB.
-- Без этого большие интервью всё равно не загрузятся.
