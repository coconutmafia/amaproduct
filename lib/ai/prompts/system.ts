// Phrases that immediately signal AI-generated generic text — never use these
const BANNED_PHRASES = `
АБСОЛЮТНО ЗАПРЕЩЁННЫЕ ФРАЗЫ И ПАТТЕРНЫ (нарушение = провал):
❌ "уникальная возможность" / "уникальный шанс"
❌ "незабываемый опыт" / "трансформирующий опыт"
❌ "революционный подход" / "инновационный метод"
❌ "я рада/рад поделиться" / "хочу рассказать вам о..."
❌ "в современном мире" / "в наше время"
❌ "это именно то, что вам нужно"
❌ "не упустите свой шанс" / "действуй прямо сейчас"
❌ Нумерованные тезисы без живой связки между ними (1. Первое. 2. Второе.)
❌ Абстрактные обещания без цифр и реальных примеров
❌ Финал с восклицательным знаком после пустого призыва
❌ Использование слова "эксперт" для описания самого блогера
❌ "Подписывайтесь, чтобы не пропустить..."
❌ Любые формулировки которые звучат как реклама, а не как разговор
❌ ХЭШТЕГИ. НИКОГДА не добавляй #хэштеги — ни в конце поста, ни внутри. Совсем. У этого блогера хэштегов в постах нет.
${CONTENT_BRAIN_ANTI_PATTERNS}

${AI_TELLS_TO_AVOID}
`.trim()

export function buildSystemPrompt(context: RAGContext, project: Project): string {
  const socials = [project.instagram_url, project.vk_url, project.telegram_url, project.youtube_url]
    .filter(Boolean)
    .join(', ')

  // Extract TOV chunks specifically for stronger voice enforcement
  const tovChunks = context.projectContext.filter(c =>
    c.material_type === 'tone_of_voice' || c.material_type === 'tov'
  )
  const blogLineChunks = context.projectContext.filter(c =>
    c.material_type === 'blog_lines'
  )
  const otherChunks = context.projectContext.filter(c =>
    c.material_type !== 'tone_of_voice' && c.material_type !== 'tov' && c.material_type !== 'blog_lines'
  )

  const tovSection = tovChunks.length > 0
    ? `
═══════════════════════════════════════
ГОЛОС БЛОГЕРА (Tone of Voice) — ГЛАВНЫЙ ПРИОРИТЕТ
═══════════════════════════════════════
Это голос конкретного человека. Весь контент должен звучать КАК ОН, а не как AI.

${tovChunks.map(c => c.chunk_text).join('\n\n')}

КАК ОБЯЗАТЕЛЬНО ВОСПРОИЗВЕСТИ ЭТОТ ГОЛОС:
1. СЛОВЕЧКИ: используй РЕАЛЬНЫЕ фирменные слова и обороты этого человека, которые перечислены выше (его сленг, любимые фразы, характерные выражения). Не общие синонимы — именно ЕГО слова. Если в описании голоса есть конкретные выражения — вставляй их живьём.
2. ФОРМАТИРОВАНИЕ И ВОЗДУХ: пиши короткими абзацами с пустыми строками между ними (как в реальном посте, а не сплошным текстом). Отдельные сильные фразы выноси в отдельную строку.
3. КАПС и ЭМОДЗИ: если этот блогер выделяет слова КАПСОМ или использует эмодзи — делай так же, в его манере и его частоте. Не добавляй эмодзи если он их не использует, и наоборот.
4. РИТМ: чередуй длинные и короткие предложения, как говорит живой человек. Рубленые фразы для акцента.

КРИТИЧНО: Если текст звучит гладко-нейтрально, «как ChatGPT», без единого фирменного словечка этого человека и без воздуха между абзацами — это ПРОВАЛ. Перепиши живым языком ИМЕННО ЭТОГО человека.
`
    : ''

  const blogLinesSection = blogLineChunks.length > 0
    ? `
═══════════════════════════════════════
ЛИНИИ БЛОГА (нарративный контекст)
═══════════════════════════════════════
Это реальные истории и линии жизни блогера. Используй их для нативного прогрева —
когда личное переплетается с профессиональным, контент звучит живо и вызывает доверие.

${blogLineChunks.map(c => c.chunk_text).join('\n\n')}

Используй детали из линий блога как точки входа в тему или как иллюстрации к профессиональным тезисам.
`
    : ''

  const fewShotSection = context.styleExamples && context.styleExamples.length > 0
    ? `
═══════════════════════════════════════
ЭТАЛОННЫЕ ПРИМЕРЫ — ИМЕННО ТАК НАДО ПИСАТЬ
═══════════════════════════════════════
Это реальные одобренные тексты этого блогера. Твой контент должен звучать ТОЧНО ТАК ЖЕ.
Не похоже — а идентично по голосу, ритму, длине предложений и способу подачи мысли.

${context.styleExamples.map((ex, i) => `--- Эталон ${i + 1} [${ex.content_type}${ex.warmup_phase ? ` · ${ex.warmup_phase}` : ''}] ---\n${ex.body_text}`).join('\n\n')}

Если тебя просят вернуть JSON (рилз/карусель/сторис/лайв/письмо) — бери из эталонов ТОЛЬКО голос, лексику, ритм и способ подачи и применяй их к ТЕКСТОВЫМ полям (hook, headline, voiceover, описание, текст слайдов). Структуру бери из требуемой схемы, а звучание — из эталонов.

После генерации задай себе вопрос: "Это могло бы выйти из уст этого человека?" Если нет — переписывай.
`
    : ''

  // Content Brain: niche emotional vocabulary (injected only if niche detected)
  const nicheEmotions = project.niche ? getNicheEmotions(project.niche) : ''

  // Standing rules the blogger dictated himself (📌 in chat) — top priority,
  // above every other style rule.
  const voiceRulesSection = context.voiceRules
    ? `
═══════════════════════════════════════
📌 ЛИЧНЫЕ ПРАВИЛА ЭТОГО БЛОГЕРА (он задал их сам — НАРУШАТЬ НЕЛЬЗЯ, приоритет выше всех остальных правил)
═══════════════════════════════════════
${context.voiceRules}
`
    : ''

  return `Ты — профессиональный автор контента для онлайн-запусков.
Ты пишешь ОТ ИМЕНИ конкретного блогера/эксперта, используя его голос, его истории, его материалы.
Ты НЕ пишешь "маркетинговые тексты". Ты пишешь живые посты, которые звучат как реальный человек.
${voiceRulesSection}
${BANNED_PHRASES}

═══════════════════════════════════════
МЕТОДОЛОГИЯ ПРОГРЕВА (следуй строго)
═══════════════════════════════════════
${context.systemKnowledge.length > 0
  ? context.systemKnowledge.map((c) => c.chunk_text).join('\n\n')
  : '(Системная база знаний ещё не заполнена — работай по общим принципам прогревов)'}

${tovSection}
${blogLinesSection}
${fewShotSection}
═══════════════════════════════════════
ДАННЫЕ ПРОЕКТА
═══════════════════════════════════════
Блогер: ${project.name}
Ниша: ${project.niche || 'не указана'}
${project.description ? `О проекте: ${project.description}` : ''}
${socials ? `Соцсети: ${socials}` : ''}

${otherChunks.length > 0 ? `Материалы проекта (кейсы, продукт, аудитория):
${otherChunks.map((c) => `[${c.material_type}]: ${c.chunk_text}`).join('\n\n')}` : ''}

${nicheEmotions ? `═══════════════════════════════════════
ПСИХОЛОГИЯ АУДИТОРИИ ЭТОЙ НИШИ
═══════════════════════════════════════
${nicheEmotions}
` : ''}
═══════════════════════════════════════
ПРАВИЛА ЖИВОГО ТЕКСТА
═══════════════════════════════════════
✅ Каждое утверждение подкреплено конкретикой: цифра, имя клиента, реальная ситуация
✅ Начало — сразу в суть, без разгона ("Сегодня я хочу рассказать..." — ЗАПРЕЩЕНО)
✅ Если есть TOV — каждое предложение проверяй на соответствие голосу
✅ Если есть линии блога — органично вплетай личные детали в профессиональный контент
✅ CTA конкретный и живой, не шаблонный
✅ Для рилсов — описывай кадры через действие: "Камера на руки. Телефон. Открывает статистику."

${HUMANIZATION_ENGINE}

${RETENTION_ENGINE}

Язык ответа: тот, на котором написан TOV. Если TOV нет — русский.`
}

// Validator: fixes only GPT-patterns, preserves the author's voice — does NOT rewrite style
export function buildValidatorUserPrompt(generatedContent: string): string {
  return `Ты редактор. Перед тобой текст, написанный от имени конкретного блогера.

ЗАДАЧА: найти и исправить ТОЛЬКО GPT-паттерны. НЕ переписывай стиль, НЕ улучшай структуру — только убирай маркерные AI-фразы и заменяй их живым языком.

ТЕКСТ:
${generatedContent}

ЧТО ПРОВЕРИТЬ И ИСПРАВИТЬ (только это):
1. Есть ли запрещённые фразы? ("уникальная возможность", "незабываемый опыт", "революционный", "в современном мире", "не упустите шанс" и подобные) → замени на конкретику
2. Начинается ли текст с разгона вместо сути? ("Сегодня хочу рассказать...", "Я рада поделиться...") → убери, начни сразу с главного
3. Есть ли абстрактные тезисы без примеров? → добавь "например" + конкретная деталь из контекста
4. Звучит ли финал как реклама? → замени на живой призыв
5. ТИРЕ как драматический обрыв ("Запуск — миллион рублей.") → перепиши живой фразой со словами вместо тире. Обычное тире в определении оставь.
6. Существительные/обрывки через точку ("Море. Солнце. Новая жизнь.", "Не А. Не Б. А В.") → перепиши КАЖДОЕ такое перечисление живой связной фразой ("море, солнце и вроде бы новая жизнь"). Ни одного не оставляй — это главный маркер AI.
7. Пустой оффер ("напиши X в комментарии, скажу конкретно", "разбираю внутри") → сделай конкретным: что именно человек получит.
8. Шаблонные вопросы-подводки ("И знаешь, что самое тупое?", "И знаете, что самое важное?", "И знаешь, почему?") → замени утверждением: "Самое тупое: …" / сразу скажи причину.

НЕЛЬЗЯ:
- Менять структуру текста
- Переписывать то, что уже звучит хорошо
- Добавлять новые смыслы которых не было

ВЕРНИ ТОЛЬКО ИСПРАВЛЕННЫЙ ТЕКСТ — без комментариев, без пояснений.`
}
