// Диагнозы по сумме баллов (0–100) — дословно из PDF.
export interface DiagnosisBand {
  min: number
  max: number
  label: string
}

export const DIAGNOSIS_BANDS: DiagnosisBand[] = [
  { min: 0,  max: 30,  label: 'Страница не продаёт: нет ясности, доверия или воронки' },
  { min: 31, max: 55,  label: 'Есть база, но смыслы и упаковка слабые' },
  { min: 56, max: 75,  label: 'Страница рабочая, но есть потери в доверии/CTA/контенте' },
  { min: 76, max: 90,  label: 'Хорошая упаковка, можно усиливать конверсию' },
  { min: 91, max: 100, label: 'Сильная маркетинговая страница' },
]

/** Диагноз по нормализованному баллу 0–100. */
export function diagnose(score100: number): string {
  const s = Math.max(0, Math.min(100, Math.round(score100)))
  return (DIAGNOSIS_BANDS.find(b => s >= b.min && s <= b.max) ?? DIAGNOSIS_BANDS[0]).label
}

