// «Чек-лист проверки блога к продажам» — единый источник правды для диагностики.
// Придумано Августой + тестером-проджектом; PDF-оригинал = 10 блоков × 5 пунктов,
// каждый пункт 0–2 балла → максимум 100 баллов. Диагноз по диапазону суммы.
//
// ВАЖНО про данные: диагностика работает по ТЕКСТУ профиля (шапка + подписи к
// последним постам) — это всё, что даёт скрейп (lib/scrape/instagramProfile.ts:
// { bio, posts[] }). Актуальные (highlights), сторис, картинки/визуал и то, КУДА
// ведёт ссылка из шапки, скрейп НЕ видит. Поэтому у каждого пункта есть флаг
// `fromText`: true — оценивается по тексту; false — честно помечаем «проверим на
// консультации» вместо того, чтобы выдумывать балл (это и есть upsell-крючок).

export interface ChecklistItem {
  /** Формулировка пункта (как в PDF). */
  label: string
  /** Можно ли честно оценить по тексту профиля (bio + посты). */
  fromText: boolean
}

export interface ChecklistBlock {
  /** Стабильный ключ — по нему матчим ответ модели, не по индексу/тексту. */
  key: string
  /** Заголовок блока (как в PDF). */
  title: string
  items: ChecklistItem[]
}

