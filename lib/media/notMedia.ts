// «Этот файл точно НЕ аудио/видео» — общая проверка для страницы исследования
// и для роута постановки задачи на расшифровку.
//
// Зачем (инцидент 17 июля): клиент на тарифе Pro загрузил в расшифровку интервью
// фотографию (.png с телефона). Файл спокойно доехал до ffmpeg, тот попытался
// вытащить звуковую дорожку из картинки и упал. Человек увидел на экране
// «Ошибка расшифровки: ffmpeg: Command failed: /var/task/node_modules/...» —
// то есть командную строку и внутренние пути. Тип файла не проверялся НИГДЕ:
// ни на клиенте, ни в upload-url, ни при постановке задачи.
//
// ⚠️ ПОЧЕМУ DENY-LIST, А НЕ ALLOW-LIST (не «улучшай» на allow-list!):
//   • у инпута на странице исследования НАМЕРЕННО нет accept — iOS Safari кидает
//     DOMException на файлах из iCloud (см. комментарий у инпута);
//   • у файла-заглушки из iCloud имя не читается вообще (`f.name` бросает), и код
//     подставляет «файл 1» — расширения нет. Allow-list зарубил бы такую загрузку,
//     хотя сегодня она работает;
//   • звук ffmpeg тянет из десятков форматов (включая видеоконтейнеры) — полный
//     allow-list всё равно оказался бы неполным и резал бы валидные записи.
// Поэтому режем только то, что человек реально приносит по ошибке: фото, скриншот,
// документ, архив. Всё остальное пропускаем — на неизвестном мусоре сработает
// ffmpeg, а его текст теперь прячет friendlyError.

const NOT_MEDIA_EXT = new Set([
  // фото и скриншоты — ровно этот случай и словили
  'png', 'jpg', 'jpeg', 'webp', 'gif', 'heic', 'heif', 'bmp', 'tiff', 'tif', 'svg', 'avif',
  // документы
  'pdf', 'doc', 'docx', 'xls', 'xlsx', 'ppt', 'pptx', 'txt', 'md', 'csv',
  'rtf', 'odt', 'pages', 'numbers', 'key', 'epub',
  // архивы и прочее
  'zip', 'rar', '7z', 'gz', 'tar', 'dmg', 'exe', 'apk', 'json', 'xml', 'html',
  // ярлыки Google Drive — iOS отдаёт ИХ, когда файл в пикере не скачан локально
  // (поймано 20 июля: клиент на Pro загрузил .gsheet — ярлык Google Таблицы, —
  // он прошёл первый deny-list как «неизвестное расширение» и дошёл до ffmpeg)
  'gdoc', 'gsheet', 'gslides', 'gdraw', 'gform', 'gtable', 'gsite', 'gnote',
  // прочие ярлыки-ссылки — внутри нет медиаданных
  'url', 'webloc', 'lnk',
])

export const NOT_MEDIA_MESSAGE =
  'Это не аудио и не видео — расшифровывать нечего. Загрузи запись интервью (mp3, m4a, wav) или видеофайл.'

/** Нормализует расширение так же, как это делают клиент и роуты (см. upload-url). */
export function normalizeExt(raw: string | undefined | null): string {
  return (raw ?? '').toLowerCase().replace(/[^a-z0-9]/g, '')
}

/**
 * true — файл заведомо не медиа и до ffmpeg его пускать не нужно.
 * Неизвестное расширение (или его отсутствие — привет, iCloud) считается
 * допустимым: лучше пропустить сомнительный файл, чем зарубить валидное интервью.
 */
export function isDefinitelyNotMedia(opts: { ext?: string | null; mime?: string | null }): boolean {
  const mime = (opts.mime ?? '').toLowerCase()
  // MIME надёжнее расширения — браузер отдаёт его для файлов из галереи.
  if (mime.startsWith('audio/') || mime.startsWith('video/')) return false
  if (mime.startsWith('image/')) return true

  return NOT_MEDIA_EXT.has(normalizeExt(opts.ext))
}
